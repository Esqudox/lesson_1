a = int(input('Введите целое положительное число - '))
# Берем за максимум последнюю цифру числа (остаток от деления на 10)
max_num = a % 10
# Здесь мы начинаем делить число на 10 и сравниваем остаток от деления с нашим максимумом
while a >= 1:
    a = a // 10;
    if a % 10 > max_num:
        max_num = a % 10
    elif a > 9:
        continue
    else:
        print(f'\nСамая большая цифра в вашем числе - {max_num}')
        break
